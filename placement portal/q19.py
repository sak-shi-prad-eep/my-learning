def find_Diameter(r):
    print(f"Diameter of a circle = {(2*r):.2f}")
def find_Circumference(r):
    print(f"Circumference of a circle = {(3.14*r*2):.2f}")
def find_Area(r):
    print(f"Area of a circle = {(3.14*r*r):.2f}")


radius=float(input())
find_Diameter(radius)
find_Circumference(radius)
find_Area(radius)